package br.fiap.modelo;

public class TipoBilhete {

	private Integer id_tipo_bilhete;
	private String tipo_passagem;

	public Integer getId_tipo_bilhete() {
		return id_tipo_bilhete;
	}

	public void setId_tipo_bilhete(Integer id_tipo_bilhete) {
		this.id_tipo_bilhete = id_tipo_bilhete;
	}

	public String getTipo_passagem() {
		return tipo_passagem;
	}

	public void setTipo_passagem(String tipo_passagem) {
		this.tipo_passagem = tipo_passagem;
	}

}
